    } catch (\Exception $e) {
        DB::rollBack(); // C√≥ l·ªói: rollback t·∫•t c·∫£

        return response()->json([
            'message' => 'ƒê√£ x·∫£y ra l·ªói, h·ªá th·ªëng ƒë√£ rollback',

           'error' => $e->getMessage(),     // <- d√≤ng n√†y l√† l·ªói c·ª• th·ªÉ
    'line' => $e->getLine(),         // <- d√≤ng g√¢y l·ªói
    'file' => $e->getFile(),
        ], 500);
    }
}



